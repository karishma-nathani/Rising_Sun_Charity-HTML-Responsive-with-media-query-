				<h1>Our Mission</h1>
				<p>The overall purpose/mission of The Rising Sun Charitable Trust is to provide financial assistance (aimed at addressing necessitous circumstances – that is, any difficulty being experienced in relation to: paying debts as they fall due and/or providing “essentials” required as part of daily life), to those families who have had Australian (Army/Navy/Air Force) soldiers:</p>
				<ul class="mission">
					<li>Killed In Action (KIA); or</li>
					<li>Lost to, or severely impacted by, Post Traumatic Stress Disorder (PTSD) related illness</li>
				</ul>
				<p><b><i>Note:</i></b> <i>Definition of “essentials” includes but is not limited to: health/medical/wellbeing, education, welfare, food, transport, clothing, household maintenance, accommodation, security, bereavement, childcare, disaster, recreation/leisure related costs.</i></p>
			</div>		
			<div class="vision-content">
				<h1>Our Vision</h1>
				<p>The Vision of The Rising Sun Charitable Trust is to become the most visible, most recognised, most trusted, most dependable, most honest, most committed, most genuine and most generous military themed charity for families affected by KIA’s and PTSD related illness.</p>
